export const totalItems = categories.reduce((sum, cat) => sum + cat.items.length, 0);

export const requiredItems = categories
  .filter(c => c.requiredFor100)
  .reduce((sum, cat) => sum + cat.items.length, 0);

export function getCategoryById(id: string): Category | undefined {
  return categories.find(c => c.id === id);
}

export function getCompletionPercent(completedIds: Set<string>, categoryId?: string): number {
  if (categoryId) {
    const cat = getCategoryById(categoryId);
    if (!cat || cat.items.length === 0) return 0;
    const done = cat.items.filter(item => completedIds.has(item.id)).length;
    return Math.round((done / cat.items.length) * 100);
  }
  const done = categories.reduce((sum, cat) => {
    return sum + cat.items.filter(item => completedIds.has(item.id)).length;
  }, 0);
  return Math.round((done / totalItems) * 100);
}
