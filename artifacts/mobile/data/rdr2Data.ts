// ─── Helpers ────────────────────────────────────────────────────────────────

export const totalItems = categories.reduce((sum, c) => sum + c.items.length, 0);

export const requiredItems = categories
  .filter(c => c.requiredFor100)
  .reduce((sum, c) => sum + c.items.length, 0);

export function getCategoryById(id: string): Category | undefined {
  return categories.find(c => c.id === id);
}

export function getCategoriesBySection(section: string): Category[] {
  return categories.filter(c => c.section === section);
}

export function getCompletionPercent(completedIds: Set<string>, categoryId?: string): number {
  if (categoryId) {
    const cat = getCategoryById(categoryId);
    if (!cat || cat.items.length === 0) return 0;
    const done = cat.items.filter(i => completedIds.has(i.id)).length;
    return Math.round((done / cat.items.length) * 100);
  }
  if (totalItems === 0) return 0;
  const done = categories.reduce(
    (sum, cat) => sum + cat.items.filter(i => completedIds.has(i.id)).length,
    0
  );
  return Math.round((done / totalItems) * 100);
}

export function getSectionPercent(completedIds: Set<string>, section: string): number {
  const cats = getCategoriesBySection(section);
  const total = cats.reduce((s, c) => s + c.items.length, 0);
  if (total === 0) return 0;
  const done = cats.reduce(
    (s, c) => s + c.items.filter(i => completedIds.has(i.id)).length,
    0
  );
  return Math.round((done / total) * 100);
}
